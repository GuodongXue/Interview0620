package PriorityQueuePreatice;

public class Customer {
    private int it;
    private String name;

    public Customer(int it, String name) {
        this.it = it;
        this.name = name;
    }

    public Customer() {
    }

    public int getIt() {
        return it;
    }

    public void setIt(int it) {
        this.it = it;
    }
}
